#include <cctype>
#include <iostream>
#include <string>

#include <ion/base/stringutils.h>

#include "Base/ActionMap.h"
#include "Base/HelpMap.h"
#include "Enums/Action.h"
#include "Enums/ActionCategory.h"
#include "Handlers/ShortcutHandler.h"
#include "Util/Enum.h"
